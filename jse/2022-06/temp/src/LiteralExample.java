
//리터럴(Literal):  "순수한 값" 자체를 의미
public class LiteralExample {
	public static void main(String[] args) {	//-----Class Block Start
		int var1 = 10;		//10진수
		System.out.println(var1);
		
		int var2 = 010;		//8진수
		System.out.println(var2);
		
		int var3 = 0x10;	//16진수
		System.out.println(var3);
	} //main
} //end class	// ----- Class Block End
