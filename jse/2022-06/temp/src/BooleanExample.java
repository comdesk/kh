
public class BooleanExample {
	
	//boolean타입을 사용해보자!: true/false(참/거짓) 값만을 가질 수 있다.
	//주로, 조건문/반복문 만들 때 사용
	public static void main(String[] args) {
		boolean stop = false;
		
		if(stop) {	//조건이 참일 때
			System.out.println("중지합니다.");
		} else {	//조건이 거짓일 때
			System.out.println("시작합니다.");
		} //if-else
	} //main
} //end class
