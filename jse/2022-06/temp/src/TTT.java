
// {...} => Block을 만들어 낸다.
public class TTT {	// 클래스 블록의 시작
		
	public static void main(String ... args) { //메소드 블록의 시작
		//메소드 블록 안에 "실행문장"(Lvalue와 Rvalue로 구성된)들을 작성
		
		System.out.println("여기는 클래스 블록입니다.");
		
		//Method Block의 끝
	} //main (Entry Point = 실행 진입점)

//클래스 블록의 끝
} //end class
//실행자체가 불가능함. Run as에서 Java application이 안 나옴.
//main이 없기 때문에 진입점이 없음.
//라이브러리 클래스